/**
 *@note: if it is currently night, day 2-5 forecast icons will be for the night forecast.
 */

bool WeatherXmlContentHandler::startDocument()
{
    minimumTemps.clear();
    maximumTemps.clear();
    weatherIconLocations.clear();
    mode = "";
    humidity = -1;
    precipitation = -1;
    isNight = false;
    weatherIconLocations.clear();
    weatherForecastDescription = "";
    //Do not reset stateShortName since this is needed after google weather API xml parse.
    //stateShortName = "";
    latitudes.clear();
    longitudes.clear();
    dewPoint = -1;
    sustainedWind = -1;

    return true;
}

/**
 * @brief WeatherXmlContentHandler::startElement
 * @param namespaceURI
 * @param localName
 * @param qName
 * @param atts
 * @return
 *
 * Contains logic for parsing Google Weather API 3.0 XML feed and weather.gov XML feed.
 *
 * Parses latitude and longitude coordinates from google weather XML feed, and all other parameters
 * below from weather.gov XML feed.
 *
 * @todo debug print commented couts below.
 */
bool WeatherXmlContentHandler::startElement(const QString & /*namespaceURI*/, const QString & localName,
                    const QString & /*qName*/, const QXmlAttributes & atts )
{
//    std::cout << "Read Start Tag : " << localName.toStdString()<< std::endl;
//    std::cout << "Tag Attributes: " << std::endl;

    if(!localName.compare("temperature") && atts.length() > 0 && !atts.value(0).compare("minimum"))
        mode = QString("minimum");
    else if(!localName.compare("temperature") && atts.length() > 0 && !atts.value(0).compare("maximum"))
        mode = QString("maximum");
    else if(!localName.compare("humidity"))
        mode = QString("humidity");
    else if(!localName.compare("probability-of-precipitation"))
        mode = QString("precipitation");
    else if(!localName.compare("weather-conditions"))
    {
        if (!isNight && weatherIconLocations.size() < 5)
        {
            if (weatherIconLocations.size() == 0)
                weatherForecastDescription = atts.value(0);

            appendIconLocation(atts.value(0));
        }
        isNight = !isNight;
    }
    else if(!localName.compare("wind-speed") && atts.length() > 0 && !atts.value(0).compare("sustained"))
        mode = QString("sustained_wind");
    else if(!localName.compare("temperature") && atts.length() > 0 && !atts.value(0).compare("dew point"))
        mode = QString("dewpoint");

    else if(!localName.compare("lat"))
        mode = QString("latitude");
    else if(!localName.compare("lng"))
        mode = QString("longitude");
    else if(!localName.compare("address_component"))
        mode = QString("address_component");
    else if(!mode.compare("address_component") && !localName.compare("short_name") )
        mode = QString("state-short-name");

//    for(int index = 0 ; index<atts.length();index++)
//    {
//        std::cout << atts.type(index).toStdString()<<"="
//      <<atts.value(index).toStdString()<< std::endl;
//    }

//    std::cout << "------------------------" << std::endl;

    return true;

}

void WeatherXmlContentHandler::appendIconLocation( const QString & weatherDescription )
{
    if (weatherDescription.contains("Freezing", Qt::CaseInsensitive) ||
            weatherDescription.contains("Snow", Qt::CaseInsensitive) ||
            weatherDescription.contains("Flurr", Qt::CaseInsensitive) ||
            weatherDescription.contains("Sleet", Qt::CaseInsensitive) ||
            weatherDescription.contains("Wintry", Qt::CaseInsensitive) )
    {
        weatherIconLocations.append(SNOW_ICON_LOCATION);
    }
    else if ( weatherDescription.contains("Thunder", Qt::CaseInsensitive) ||
              weatherDescription.contains("Storm", Qt::CaseInsensitive) )
    {
        weatherIconLocations.append(STORM_ICON_LOCATION);
    }
    else if ( weatherDescription.contains("Rain", Qt::CaseInsensitive) )
    {
        weatherIconLocations.append(RAIN_ICON_LOCATION);
    }
    else if ( weatherDescription.contains("Showers", Qt::CaseInsensitive) ||
              weatherDescription.contains("Sprinkle", Qt::CaseInsensitive) ||
              weatherDescription.contains("Drizzle", Qt::CaseInsensitive) )
    {
        weatherIconLocations.append(SHOWERS_ICON_LOCATION);
    }
    else if ( weatherDescription.contains("Cloudy", Qt::CaseInsensitive) ||
              weatherDescription.contains("Partly Sunny", Qt::CaseInsensitive) ||
              weatherDescription.contains("Overcast", Qt::CaseInsensitive) )
    {
        weatherIconLocations.append(CLOUDS_ICON_LOCATION);
    }
    else
    {
        weatherIconLocations.append(SUN_ICON_LOCATION);
    }
}

void WeatherXmlContentHandler::setParameter( QList<int>* param, const QString & ch, int maxParamSize )
{
    bool ok;
    int value = ch.toInt(&ok);
    if (ok && param->size() < maxParamSize)
        param->append(value);
    if (param->size() == maxParamSize)
        mode = "";
}

void WeatherXmlContentHandler::setParameter( QList<double>* param, const QString & ch, int maxParamSize )
{
    bool ok;
    double value = ch.toDouble(&ok);
    if (ok && param->size() < maxParamSize)
        param->append(value);
    if (param->size() == maxParamSize)
        mode = "";
}

void WeatherXmlContentHandler::setParameter( int* param, const QString & ch )
{
    bool ok;
    int value  = ch.toInt(&ok);
    if (ok)
    {
        *param = value;
        mode = "";
    }
}

void WeatherXmlContentHandler::setParameter( QString * param, const QString & ch )
{
    *param = ch;
    mode = "";
}


bool WeatherXmlContentHandler::characters ( const QString & ch )
{
//    std::cout << "Characters: " << ch.toStdString() << std::endl;

    //Store 5 days of minimum temps.
    if (! mode.compare("minimum"))
        setParameter( &minimumTemps, ch, 5 );

    //Store 5 days of maximum temps.
    else if (! mode.compare("maximum"))
        setParameter( &maximumTemps, ch, 5 );

    //Store current humidity.
    else if (! mode.compare("humidity"))
        setParameter( &humidity, ch );

    //Store current precipitation chance.
    else if (! mode.compare("precipitation"))
        setParameter( &precipitation, ch );

    //Store latitude coordinates of current zip code.
    else if (! mode.compare("latitude"))
        setParameter( &latitudes, ch, 1 );

    //Store longitude coordinates of current zip code.
    else if (! mode.compare("longitude"))
        setParameter( &longitudes, ch, 1 );

    //Store current dewpoint.
    else if (! mode.compare("dewpoint"))
        setParameter( &dewPoint, ch );

    //Store current wind speed.
    else if (!mode.compare("sustained_wind"))
        setParameter( &sustainedWind, ch );

    //Store state as 2 letter abbreviation.
    else if (!mode.compare("state-short-name") && ch.size() == 2 && ch.compare("US"))
        setParameter( &stateShortName, ch );

    //After the state is parsed the only other information that patches the parse parameters is 'US'.
    //Reset the mode since we do not want to store 'US'.
    else if (!mode.compare("state-short-name") && !ch.compare("US"))
        mode = "";

    return true;
}
